
import User from '../models/user.model.js';
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";


// Register a new user     --->  POST/api/auth/register
 const registerUser= async(req,res)=>{

     try{
         // Get user details from request body
        const {username,email,password}=req.body;


         // validate fields are not empty
         if(!username|| ! email || !password){
            return res.status(400).json({message:"All fields are required"});
         }
        // Check if user already exists
        const existingUser= await User.findOne({email});
        if(existingUser){
            return res.status(400).json({message:"User already exists"});
        }
          // Step 4 — Hash the password before saving
         // 10 = salt rounds (how strong the hash is)
         // NEVER save plain password in DB
         const hashedPassword=await bcrypt.hash(password,10);

         //create and save user in DB
         const user= await User.create({
            username,
            email,
            password:hashedPassword
         });
          // send success response
         res.status(201).json({message:"User registered successfully"});

     }catch(error) {
  console.error("Error registering user:", error.message)
  // ← change this line temporarily
  res.status(500).json({ message: error.message })
}

 }

     
   // User login and authentication will be implemented here    //POST/api/auth/login

   const loginUser= async (req,res)=>{
     
      try{

        // input 
        const {email,password}=req.body;

        // validate fileds are not empty
        if(!email || ! password){
            return res.status(400).json({message:"All fields are required"});
         }

         // find user by email  if user not found return error
         const user= await User.findOne({email});   //  user._id is the unique ID generated by MongoDB for each user
         if(!user){
            return res.status(404).json({message:"User not found"});
         }
         
         // Step 4 — Compare entered password with hashed password in DB
         // bcrypt.compare returns true or false
        const isPasswordValid= await bcrypt.compare(password,user.password);
        if(!isPasswordValid){
            return res.status(401).json({message:"Invalid credentials"});
        }

         // Step 5 — Create JWT token
         // payload = data stored inside token
         // JWT_SECRET = secret key from .env
         // expiresIn = token expires after 7 days
        const token=jwt.sign(
            {userId:user._id},  // payload
            process.env.JWT_SECRET,  // secret key
            {expiresIn:"7d"}  // token expiration
         ); 
        
        // Step 6 — Send token + user info to frontend
          // Frontend saves this token in localStorage

          res.status(200).json({
            token,
            user:{
                id:user._id,
                username:user.username,
                email:user.email
            }
          })
      }
      catch(error){
            console.error("Error logging in user:", error);
            res.status(500).json({ message: "Internal server error" });
      }
   }



     // LOGOUT
    // POST /api/auth/logout
   const logoutUser = (req, res) => {
  try {
    res.status(200).json({ message: "Logged out successfully" })
  } catch (error) {
    console.error("Error logging out user:", error)
    res.status(500).json({ message: "Internal server error" })
  }
}



export {registerUser,loginUser,logoutUser};